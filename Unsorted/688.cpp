bool f(int N)
{
	if(N<n && isprime[N])
		return false;

	int l=N/2, r=N-N/2;
	start:
	while( l!=1 && (M[l]||M[r]||(l==r&&(isprime[l]))) )
	{
		--l; ++r;
	}
	if(l*r<=N) 
	{
		return false;
	}
	bool L=f(l);
	if(L==false && l!=r)
	{
		ans.push_back(l);
		M[l]=1;
	}
	bool R=f(r);
	if(!(L|R) && l==r)
	{
		l--;
		r++;
		goto start;
	}
	if(R==false)
	{
		ans.push_back(r);
		M[r]=1;
	}
//	for(auto x:ans)
//		cout<<x<<" ";
//	cout<<endl;
	return true;
}

